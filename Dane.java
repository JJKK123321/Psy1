package com.example.psy;

public class Dane {
    private String rasa, informacje, piesid;
    private Integer wiek;

    public Dane() {

    }

    public String getRasa() {
        return rasa;
    }

    public void setRasa(String rasa) {
        this.rasa = rasa;
    }

    public String getInformacje() {
        return informacje;
    }

    public void setInformacje(String informacje) {
        this.informacje = informacje;
    }

    public String getPiesid() {
        return piesid;
    }

    public void setPiesid(String piesid) {
        this.piesid = piesid;
    }

    public Integer getWiek() {
        return wiek;
    }

    public void setWiek(Integer wiek) {
        this.wiek = wiek;
    }
}